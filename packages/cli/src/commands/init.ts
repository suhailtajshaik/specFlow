async function createExampleSpecs(workingDir: string): Promise<void> {
  // Create example business requirements
  const userRegistrationReq = `# User Registration Ideas

Just some rough notes on user registration:

- Users should be able to sign up with email and password
- Need to validate emails are real
- Passwords should be secure (not sure exact rules yet)
- Send verification email after signup
- Don't allow duplicate emails
- Return user info after successful registration

## Security thoughts
- Rate limit registration attempts
- Hash passwords obviously
- Maybe block disposable email domains?

## Questions
- What happens if verification email fails to send?
- Should we allow social login later?
`;

  const createOrderReq = `# Order Creation

Basic e-commerce order flow:

• User must be logged in
• User selects products and quantities  
• System calculates total price
• User provides payment info
• System processes payment
• Order is created and confirmed
• User gets order confirmation

**Business rules:**
- Check product stock before creating order
- Don't trust price from client - calculate server-side
- Reserve inventory during order process
- Handle payment failures gracefully
- Send confirmation email

**Edge cases:**
- What if product goes out of stock during checkout?
- Payment succeeds but email fails?
- User closes browser during payment?
`;

  writeFileSync(
    join(workingDir, 'requirements/user-registration-notes.md'), 
    userRegistrationReq
  );

  writeFileSync(
    join(workingDir, 'requirements/order-creation-ideas.md'), 
    createOrderReq
  );

  // Create a simple schema example
  const userSchema = {
    type: "object",
    properties: {
      id: { type: "string", format: "uuid" },
      email: { type: "string", format: "email" },
      firstName: { type: "string" },
      lastName: { type: "string" }
    }
  };

  writeFileSync(
    join(workingDir, 'requirements/schemas/user-basic.json'),
    JSON.stringify(userSchema, null, 2)
  );
}