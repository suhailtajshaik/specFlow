import { writeFileSync, mkdirSync, existsSync, copyFileSync } from 'fs';
import { join } from 'path';
import { Logger } from '../ui/logger.js';
import { Spinner } from '../ui/spinner.js';
import inquirer from 'inquirer';

interface InitOptions {
  example?: boolean;
  provider?: 'gemini' | 'claude' | 'llamacpp';
}

export async function initCommand(projectName?: string, options: InitOptions = {}) {
  try {
    Logger.header('Initialize SpecFlow Project');

    // Get project name if not provided
    if (!projectName) {
      const answer = await inquirer.prompt([
        {
          type: 'input',
          name: 'name',
          message: 'Project name:',
          default: 'my-api',
          validate: (input: string) => {
            if (!input.trim()) {
              return 'Project name is required';
            }
            return true;
          }
        }
      ]);
      projectName = answer.name;
    }

    // At this point, projectName is guaranteed to be defined
    const finalProjectName: string = projectName!;

    // Check if we're in the project directory or need to create it
    const currentDir = process.cwd();
    const projectDir = join(currentDir, finalProjectName);
    const useCurrentDir = currentDir.endsWith(finalProjectName) || finalProjectName === '.';
    
    const workingDir = useCurrentDir ? currentDir : projectDir;

    if (!useCurrentDir) {
      if (existsSync(projectDir)) {
        Logger.error(`Directory '${finalProjectName}' already exists`);
        return;
      }
      mkdirSync(projectDir, { recursive: true });
      Logger.success(`Created directory: ${finalProjectName}`);
    }

    await Spinner.withSpinner('Setting up project structure...', async (spinner) => {
      // Create directory structure
      const dirs = [
        'requirements/business',
        'requirements/technical', 
        'requirements/schemas'
      ];

      for (const dir of dirs) {
        mkdirSync(join(workingDir, dir), { recursive: true });
      }

      spinner.updateText('Creating configuration files...');

      // Create specflow.config.yaml
      const config = createConfigFile(finalProjectName, options.provider || 'gemini');
      writeFileSync(join(workingDir, 'specflow.config.yaml'), config);

      // Create .gitignore
      const gitignore = createGitignoreFile();
      writeFileSync(join(workingDir, '.gitignore'), gitignore);

      // Create README.md
      const readme = createReadmeFile(finalProjectName);
      writeFileSync(join(workingDir, 'README.md'), readme);

      if (options.example) {
        spinner.updateText('Adding example specifications...');
        await createExampleSpecs(workingDir);
      }
    });

    Logger.success('Project initialized successfully!');
    
    // Show next steps
    Logger.header('Next Steps');
    
    if (!useCurrentDir) {
      Logger.info(`1. cd ${finalProjectName}`);
    } else {
      Logger.info('1. You\'re in the project directory');
    }

    if (options.example) {
      Logger.info('2. Review the example specs in requirements/');
      Logger.info('3. Run `specflow prepare` to refine the specifications');
    } else {
      Logger.info('2. Create your rough specifications in requirements/');
      Logger.info('   • Add .md files with your API ideas (bullet points, notes, anything!)');
      Logger.info('3. Run `specflow prepare` to transform them into professional specs');
    }
    
    Logger.info('4. Run `specflow generate` to create the backend code');
    Logger.info('5. Run `specflow dev` to start the development server');

    // Show LLM setup reminder
    const hasApiKey = process.env.GEMINI_API_KEY || process.env.ANTHROPIC_API_KEY || process.env.LLAMACPP_API_KEY;
    if (!hasApiKey) {
      Logger.divider();
      Logger.warning('LLM API Key Required');
      Logger.info('To use `specflow prepare`, you\'ll need an LLM provider API key:');
      Logger.info('• Gemini (free): https://ai.google.dev');
      Logger.info('• Claude: https://console.anthropic.com');
      Logger.info('• Or run local LlamaCpp');
    }

  } catch (error) {
    Logger.error(`Failed to initialize project: ${error instanceof Error ? error.message : 'Unknown error'}`);
    process.exit(1);
  }
}

function createConfigFile(projectName: string, provider: string): string {
  return `# SpecFlow Configuration
version: "1.0"

# Project info
project:
  name: "${projectName}"
  description: "API generated by SpecFlow"
  version: "1.0.0"

# LLM Provider for spec preparation and code generation
llm:
  provider: "${provider}"           # gemini | claude | llamacpp
  model: ""                        # auto-selects best model per provider
  apiKey: ""                       # or set via GEMINI_API_KEY / ANTHROPIC_API_KEY env var

# Generation output
output:
  directory: "./generated"         # where generated code goes
  runtime: "bun"                   # bun (default)
  framework: "hono"                # hono (default)
  orm: "drizzle"                   # drizzle (default)
  database: "postgresql"           # postgresql (default)
  includeDocker: true              # generate Dockerfile + docker-compose.yml
  includeTests: false              # generate test files (Phase 2)

# Requirements location
requirements:
  directory: "./requirements"      # where spec files live
  businessDir: "business"          # subdirectory for .req.md files
  technicalDir: "technical"        # subdirectory for .contract.md files
  schemasDir: "schemas"            # subdirectory for shared JSON schemas
`;
}

